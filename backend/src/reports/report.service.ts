  async generateReportPdf(
    payload: PrintReportPayload,
  ): Promise<{ buffer: Buffer; filename: string }> {
    const model = this.resolvePrintModel(payload);
    const html = this.buildHtmlTemplate(model);

    const browser = await puppeteer.launch({
      headless: true,
      args: ['--no-sandbox', '--disable-setuid-sandbox'],
    });

    try {
      const page = await browser.newPage();
      await page.setContent(html, { waitUntil: 'networkidle0' });

      const pdf = await page.pdf({
        format: 'A4',
        printBackground: true,
        displayHeaderFooter: true,
        margin: {
          top: '64px',
          bottom: '64px',
          left: '20px',
          right: '20px',
        },
        headerTemplate: `
          <div style="width:100%; padding:0 20px; font-size:9px; color:#475569; text-align:center;">
            FeedFactory Pro - ${this.escapeHtml(model.title)}
          </div>
        `,
        footerTemplate: `
          <div style="width:100%; padding:0 20px; font-size:9px; color:#64748b; display:flex; justify-content:space-between;">
            <span>Generated by FeedFactory Pro</span>
            <span>Page <span class="pageNumber"></span> / <span class="totalPages"></span></span>
          </div>
        `,
      });

      return {
        buffer: Buffer.from(pdf),
        filename: this.buildFilename(payload.filename || payload.title || `${payload.type}-report`),
      };
    } finally {
      await browser.close();
    }
  }

