  private defaultTitle(type: PrintableReportType): string {
    if (type === 'dashboard') return 'Dashboard Report';
    if (type === 'items') return 'Items Report';
    return 'Transactions Report';
  }

  private resolveAlign(value: unknown): 'left' | 'center' | 'right' {
    if (value === 'center' || value === 'right') return value;
    return 'left';
  }

  private formatCellValue(value: unknown): string {
    if (value == null) return '';
    if (value instanceof Date) return value.toISOString();
    if (typeof value === 'number') {
      return new Intl.NumberFormat('en-US', { maximumFractionDigits: 3 }).format(value);
    }
    if (typeof value === 'boolean') return value ? 'Yes' : 'No';
    if (Array.isArray(value)) return value.map((entry) => String(entry)).join(', ');
    if (typeof value === 'object') return JSON.stringify(value);
    return String(value);
  }

  private escapeHtml(value: unknown): string {
    return String(value ?? '')
      .replace(/&/g, '&amp;')
      .replace(/</g, '&lt;')
      .replace(/>/g, '&gt;')
      .replace(/"/g, '&quot;')
      .replace(/'/g, '&#39;');
  }

  private buildFilename(name: string): string {
    const normalized = String(name || 'report')
      .trim()
      .toLowerCase()
      .replace(/[^a-z0-9-_]+/g, '-')
      .replace(/-+/g, '-')
      .replace(/^-|-$/g, '');
    return normalized || 'report';
  }
